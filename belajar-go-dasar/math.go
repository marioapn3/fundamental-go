package main

import "fmt"

func main() {
	var result = 10 * 10
	fmt.Println(result)

	var a = 10
	var b = 10
	fmt.Println(a * b)

	var i = 10
	i += 10

	fmt.Println(i)

	i++
	fmt.Println(i)

	i--
	fmt.Println(i)

	var negative = -100
	var positive = +100
	fmt.Println(negative)
	fmt.Println(positive)

	var decimal = 2.62
	fmt.Println(decimal)

}
